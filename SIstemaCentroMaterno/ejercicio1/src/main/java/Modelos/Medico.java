package Modelos;

public class Medico {

    private int id_doctor;
    private String nombre_M;
    private String apellido_M;
    private String telefono_M;
    private String correo;
    private String especialidad_id;
    private String especialidad_descrip;
    private String nombreCompleto_M; 

    public int getId_doctor() {
        return id_doctor;
    }

    public void setId_doctor(int id_doctor) {
        this.id_doctor = id_doctor;
    }

    public String getNombre_M() {
        return nombre_M;
    }

    public void setNombre_M(String nombre_M) {
        this.nombre_M = nombre_M;
    }

    public String getApellido_M() {
        return apellido_M;
    }

    public void setApellido_M(String apellido_M) {
        this.apellido_M = apellido_M;
    }

    public String getTelefono_M() {
        return telefono_M;
    }

    public void setTelefono_M(String telefono_M) {
        this.telefono_M = telefono_M;
    }

    public String getCorreo() {
        return correo;
    }

    public void setCorreo(String correo) {
        this.correo = correo;
    }

    public String getEspecialidad_id() {
        return especialidad_id;
    }

    public void setEspecialidad_id(String especialidad_id) {
        this.especialidad_id = especialidad_id;
    }

    public String getEspecialidad_descrip() {
        return especialidad_descrip;
    }

    public void setEspecialidad_descrip(String especialidad_descrip) {
        this.especialidad_descrip = especialidad_descrip;
    }

    public String getNombreCompleto_M() {
        return nombreCompleto_M;
    }

    public void setNombreCompleto_M(String nombreCompleto_M) {
        this.nombreCompleto_M = nombreCompleto_M;
    }

    
    
}
